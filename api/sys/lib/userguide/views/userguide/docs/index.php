
<h1>Project Overview</h1>
<p>Project Details: This mobile app for iPad will help to provide employees time management for payroll purposes. Finger print readers as well as RFID readers will be integrated to the system. Main goal is to keep track of the punch in and punch out of hours for employees. This data will later be exported via ACII file to be imported into our payroll system "FAMOUSSOFTWARE"</p>

The app will use RFID and fingerprints for:
<ul>
    <li> Register employees; </li>
    <li> Track of the punch in and punch out of hours and results of the employees work.
    This app will use backend system and web portal for collecting and storing the data, PDF export and ASCII file export.
    </li>
</ul>

